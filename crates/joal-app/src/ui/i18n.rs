#[derive(Debug, Default, Clone, Copy, PartialEq, Eq)]
pub enum Language {
    #[default]
    Chinese,
    English,
}

impl Language {
    pub fn label(self) -> &'static str {
        match self {
            Self::Chinese => "中文",
            Self::English => "EN",
        }
    }

    pub fn toggle(self) -> Self {
        match self {
            Self::Chinese => Self::English,
            Self::English => Self::Chinese,
        }
    }
}

